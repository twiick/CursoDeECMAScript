// Concatenar strings
let hello="Hello";
let world="world";
let epicPhrase=hello+", "+world;
console.log(epicPhrase);

// Template literals
let epicPhrase2=`${hello}, ${world}`;
console.log(epicPhrase2);

// Multiline string con \n
let lorem="Esto es un string\nEsto es otra linea";
console.log(lorem);

// Multiline string con ``
let lorem2=`Esto es un string
Esto es otra linea`;
console.log(lorem2);